function describeCountry(country, population ,capitalCity){
return `${country} has ${population} people and its capital city is ${capitalCity}`;
}



console.log(describeCountry("Isreal" , "874,186","Jerusalem" ));
console.log(describeCountry("Usa" , "692,683","Washington" ));
console.log(describeCountry("England" , "8.982 milion","London" ));